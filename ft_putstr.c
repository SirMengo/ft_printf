#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	i;
	if(!str)
	{
		write(1,"(null)", 6);
		return (6);
	}
	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
	return (i);
}

